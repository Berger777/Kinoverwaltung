package Entities;


public class Kategorie {

  private String aufschlag;
  private String bezeichnung;
  private String kategorieId;

  public String getAufschlag() {
    return aufschlag;
  }

  public void setAufschlag(String aufschlag) {
    this.aufschlag = aufschlag;
  }

  public String getBezeichnung() {
    return bezeichnung;
  }

  public void setBezeichnung(String bezeichnung) {
    this.bezeichnung = bezeichnung;
  }

  public String getKategorieId() {
    return kategorieId;
  }

  public void setKategorieId(String kategorieId) {
    this.kategorieId = kategorieId;
  }
}
